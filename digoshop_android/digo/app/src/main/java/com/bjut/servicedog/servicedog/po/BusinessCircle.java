public class BusinessCircle {
    //商圈
    private String mn;
    private String mc;
    private String md;
    private String mid;
    private String mba;


    public String getMba() {
        return mba;
    }

    public BusinessCircle setMba(String mba) {
        this.mba = mba;
        return this;
    }

    public String getMn() {
        return mn;
    }

    public void setMn(String mn) {
        this.mn = mn;
    }

    public String getMc() {
        return mc;
    }

    public void setMc(String mc) {
        this.mc = mc;
    }

    public String getMd() {
        return md;
    }

    public void setMd(String md) {
        this.md = md;
    }

    public String getMid() {
        return mid;
    }

    public void setMid(String mid) {
        this.mid = mid;
    }
}
